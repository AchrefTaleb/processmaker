<template>
    <div class="data-table">
        <div class="card card-body table-card">
            <vuetable
                    :dataManager="dataManager"
                    :sortOrder="sortOrder"
                    :css="css"
                    :api-mode="false"
                    @vuetable:pagination-data="onPaginationData"
                    :fields="fields"
                    :data="data"
                    data-path="data"
                    pagination-path="meta"
            >
                <template slot="name" slot-scope="props">{{props.rowData.name}}</template>

                <template slot="owner" slot-scope="props">
                    <avatar-image
                            class="d-inline-flex pull-left align-items-center"
                            size="25"
                            :input-data="props.rowData.user"
                            hide-name="true"
                    ></avatar-image>
                </template>

                <template slot="actions" slot-scope="props">
                    <div class="actions">
                        <div class="popout">
                            <b-btn
                                    variant="link"
                                    @click="onAction('edit-designer', props.rowData, props.rowIndex)"
                                    v-b-tooltip.hover
                                    title="Open Modeler"
                                    v-if="permission.includes('edit-processes') && props.rowData.status === 'ACTIVE'"
                            >
                                <i class="fas fa-pen-square fa-lg fa-fw"></i>
                            </b-btn>
                            <b-btn
                                    variant="link"
                                    @click="onAction('edit-item', props.rowData, props.rowIndex)"
                                    v-b-tooltip.hover
                                    title="Config"
                                    v-if="permission.includes('edit-processes') && props.rowData.status === 'ACTIVE'"
                            >
                                <i class="fas fa-cog fa-lg fa-fw"></i>
                            </b-btn>
                            <b-btn
                                    variant="link"
                                    @click="onAction('remove-item', props.rowData, props.rowIndex)"
                                    v-b-tooltip.hover
                                    title="Archive"
                                    v-if="permission.includes('archive-processes') && props.rowData.status === 'ACTIVE'"
                            >
                                <i class="fas fa-download fa-lg fa-fw"></i>
                            </b-btn>
                            <b-btn
                                    variant="link"
                                    @click="onAction('restore-item', props.rowData, props.rowIndex)"
                                    v-b-tooltip.hover
                                    title="Restore"
                                    v-if="permission.includes('archive-processes') && props.rowData.status === 'INACTIVE'"
                            >
                                <i class="fas fa-upload fa-lg fa-fw"></i>
                            </b-btn>

                        </div>
                    </div>
                </template>
            </vuetable>

            <pagination
                    single="Process"
                    plural="Processes"
                    :perPageSelectEnabled="true"
                    @changePerPage="changePerPage"
                    @vuetable-pagination:change-page="onPageChange"
                    ref="pagination"
            ></pagination>
        </div>
    </div>
</template>

<script>
    import datatableMixin from "../../components/common/mixins/datatable";

    export default {
        mixins: [datatableMixin],
        props: ["filter", "id", "status", "permission"],
        data() {
            return {
                orderBy: "name",
                sortOrder: [
                    {
                        field: "name",
                        sortField: "name",
                        direction: "asc"
                    }
                ],

                fields: [
                    {
                        title: "Name",
                        name: "__slot:name",
                        field: "name",
                        sortField: "name"
                    },
                    {
                        title: "Category",
                        name: "category.name",
                        sortField: "category.name"
                    },
                    {
                        title: "Owner",
                        name: "__slot:owner",
                        callback: this.formatUserName
                    },
                    {
                        title: "Modified",
                        name: "updated_at",
                        sortField: "updated_at",
                        callback: "formatDate"
                    },
                    {
                        title: "Created",
                        name: "created_at",
                        sortField: "created_at",
                        callback: "formatDate"
                    },
                    {
                        name: "__slot:actions",
                        title: ""
                    }
                ]
            };
        },

        methods: {
            goToEdit(data) {
                window.location = "/processes/" + data + "/edit";
            },
            goToDesigner(data) {
                window.location = "/modeler/" + data;
            },
            onAction(action, data, index) {
                switch (action) {
                    case "edit-designer":
                        this.goToDesigner(data.id);
                        break;
                    case "edit-item":
                        this.goToEdit(data.id);
                        break;
                    case "restore-item":
                        ProcessMaker.apiClient
                            .put("processes/" + data.id + "/restore")
                            .then(response => {
                                ProcessMaker.alert("The process was restored successfully", "success");
                                this.$emit("reload");
                            });
                        break;
                    case "remove-item":
                        ProcessMaker.confirmModal(
                            "Caution!",
                            "<b>Are you sure you want to archive the process: </b>'" + data.name + "'?",
                            "",
                            () => {
                                ProcessMaker.apiClient
                                    .delete("processes/" + data.id)
                                    .then(response => {
                                        ProcessMaker.alert("Process Archived", "warning");
                                        this.$emit("reload");
                                    });
                            }
                        );
                        break;
                }
            },
            formatStatus(status) {
                status = status.toLowerCase();
                let bubbleColor = {
                    active: "text-success",
                    inactive: "text-danger",
                    draft: "text-warning",
                    archived: "text-info"
                };
                let response =
                    '<i class="fas fa-circle ' + bubbleColor[status] + ' small"></i> ';
                status = status.charAt(0).toUpperCase() + status.slice(1);
                return '<div style="white-space:nowrap">' + response + status + "</div>";
            },
            formatUserName(user) {
                return (
                    (user.avatar
                        ? this.createImg({
                            src: user.avatar,
                            class: "rounded-user"
                        })
                        : '<i class="fa fa-user rounded-user"></i>') +
                    "<span>" +
                    user.fullname +
                    "</span>"
                );
            },
            createImg(properties) {
                let container = document.createElement("div");
                let node = document.createElement("img");
                for (let property in properties) {
                    node.setAttribute(property, properties[property]);
                }
                container.appendChild(node);
                return container.innerHTML;
            },
            fetch() {
                this.loading = true;
                //change method sort by user
                this.orderBy = this.orderBy === "user" ? "user.firstname" : this.orderBy;
                //change method sort by slot name
                this.orderBy = this.orderBy === "__slot:name" ? "name" : this.orderBy;

                let url = (this.status === null || this.status === '' || this.status === undefined)
                    ? 'processes?'
                    : 'processes?status=' + this.status + '&';

                // Load from our api client
                ProcessMaker.apiClient
                    .get( url +
                        "page=" +
                        this.page +
                        "&per_page=" +
                        this.perPage +
                        "&filter=" +
                        this.filter +
                        "&order_by=" +
                        this.orderBy +
                        "&order_direction=" +
                        this.orderDirection +
                        "&include=category,user"
                    )
                    .then(response => {
                        this.data = this.transform(response.data);
                        this.loading = false;
                    });
            }
        },

        computed: {}
    };
</script>

